func TestState_Decode(t *testing.T) {
	var state State
	err := bin.NewBorshDecoder(mockRawState).Decode(&state)
	require.NoError(t, err)

	// spot check parameters that stay the same across tests (accounts do not)
	assert.Equal(t, uint8(249), state.Nonce)
	assert.Equal(t, configVersion, state.Config.Version)
	assert.Equal(t, uint8(2), state.Config.F)
	assert.Equal(t, uint8(17), state.Config.N)
	assert.Equal(t, uint8(1), state.Config.Version)
	assert.Equal(t, "1", state.Config.MinAnswer.DecimalString())
	assert.Equal(t, "1000", state.Config.MaxAnswer.DecimalString())
	assert.Equal(t, "ETH/BTC", string(bytes.Trim(state.Config.Description[:], "\x00")))
	assert.Equal(t, uint8(2), state.Config.F)
	assert.Equal(t, uint8(17), state.Config.N)
	assert.Equal(t, uint32(0), state.Config.Epoch)
	assert.Equal(t, uint8(0), state.Config.Round)
	assert.True(t, bytes.Equal([]byte{0, 3, 149, 169, 207, 0, 13, 167, 6, 71, 10, 93, 208, 36, 244, 39, 182, 193, 42, 222, 222, 212, 77, 137, 80, 144, 188, 1, 112, 131, 154, 195}, state.Config.LatestConfigDigest[:]))
}

