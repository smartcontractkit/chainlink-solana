func TestState_Decode(t *testing.T) {
	var state State
	err := bin.NewBorshDecoder(mockState.Raw).Decode(&state)
	require.NoError(t, err)

	// spot check parameters that stay the same across tests (accounts do not)
	assert.Equal(t, configVersion, state.Version)
	assert.Equal(t, uint8(252), state.Nonce)
	assert.Equal(t, uint8(6), state.Config.F)
	assert.Equal(t, "1", state.Config.MinAnswer.DecimalString())
	assert.Equal(t, "1000", state.Config.MaxAnswer.DecimalString())
	// assert.Equal(t, "ETH/BTC", string(bytes.Trim(state.Config.Description[:], "\x00")))
	assert.Equal(t, uint32(0), state.Config.Epoch)
	assert.Equal(t, uint8(0), state.Config.Round)
	assert.Equal(t, mockState.ConfigDigestHex,
		hex.EncodeToString(state.Config.LatestConfigDigest[:]))
	assert.Equal(t, uint64(19), state.Oracles.Len)
	assert.Equal(t, 19, len(state.Oracles.Data()))
	assert.Equal(t, uint64(2), state.OffchainConfig.Version)
	assert.Equal(t, uint64(6), state.OffchainConfig.Len)
	assert.True(t, bytes.Equal(
		[]byte{4, 5, 6, 4, 5, 6},
		state.OffchainConfig.Data()))
}

